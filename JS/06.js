//give string is 
console.log("String :" + firstName);

//accessing the specific character from the string
console.log("Char at 3 :" + firstName[3]);


//finding the length of the string
console.log("Length of string  : " + firstName.length);

//print the last index of the string 
console.log("Last index :" + firstName[firstName.length-1]);
